package com.view.panel;

/**
 * Enumeration used to facilitate the manipulation of panels.
 */
public enum PanelIdentifier {
	/**
	 * Starting panel identifier.
	 */
	STARTING_PANEL,
	/**
	 * Player info panel identifier.
	 */
	PLAYER_INFO_PANEL,
	/**
	 * Attribute panel identifier.
	 */
	ATTRIBUTE_PANEL,
	/**
	 * Field attribution panel identifier.
	 */
	FIELD_ATTRIBUTION_PANEL,
	/**
	 * Global field panel identifier.
	 */
	GLOBAL_FIELD_PANEL
}
